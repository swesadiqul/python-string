# We will discuss in this session, how to access string
# string indexing are divided into 2 categories. One thing is positive and second thing is negative
# positive indexing is started value 0 and negative indexing is -1

a="Bangladesh"
print(a[0])

print(a[1])

#you know negative indexing is started from the right and positive indexing is started from the left side. 
